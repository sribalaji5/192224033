#include <stdio.h>
int main()
{
	int n1,n2,merge,i, j;
	int a[i]={1,2,3,4,5,6};
	n1=sizeof(a)/sizeof(a[0]);
	int b[j]={7,8,9,10};
	n2=sizeof(a)/sizeof(a[0]);
	merge=n1+n2;
	int c[merge];
	for(i=0;i<n1; i++) 
	{
		c[i]=a[i];
	}
	for(i=0;j=n1;j++)
	(j<merge && i<n2,i++,j++);
		{
			c[j]=b[i];
		}
		for(i=0;i<merge;i++)
		{
			printf("Merging of two array:%d",c[i]);
		}
	return 0;
}

